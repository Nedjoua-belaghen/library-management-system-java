    private void ShowBooks(){
      String result ="";
      for(Book b: lib.Books){
       result += "  id: " + b.id +
               "  Name: " + b.name +
               "  Type: " + b.type + 
                "  Status: " + (b.borrowed ? "Borrowed" :"Available") + 
               "\n";
       
      }
    jTextArea1.setText(result);
    }

